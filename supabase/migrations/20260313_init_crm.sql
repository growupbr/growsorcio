create or replace function public.reset_identity_sequences()
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  perform setval(pg_get_serial_sequence('public.leads', 'id'), coalesce((select max(id) from public.leads), 0) + 1, false);
  perform setval(pg_get_serial_sequence('public.interacoes', 'id'), coalesce((select max(id) from public.interacoes), 0) + 1, false);
  perform setval(pg_get_serial_sequence('public.cadencia_itens', 'id'), coalesce((select max(id) from public.cadencia_itens), 0) + 1, false);
end;
$$;
