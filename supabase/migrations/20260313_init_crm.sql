on public.organizations
for select
to authenticated
using (public.is_org_member(id));

create policy organizations_update_owner_admin
on public.organizations
for update
to authenticated
using (public.user_role(id) in ('owner', 'admin'))
with check (public.user_role(id) in ('owner', 'admin'));

